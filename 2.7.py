n = int(input('Введите число: '))
s1 = 0
s2 = n * (n + 1) // 2


while n > 0:
    s1 += n
    n -= 1


if s2 == s1:
    print(f'равенство 1+2+...+n = n(n+1)/2 для числа {n} верно: \n {s1} = {s2}')
else:
    print (f'Сломали пайтон) равенство 1+2+...+n = n(n+1)/2 для числа {n} не верно: \n {s1} = {s2}')
